var a = 4
let b = 2

a += b
print(a)

a -= b
print(a)

a *= b
print(a)

a /= b
print(a)

a %= b
print(a)

a <<= b
print(a)

a >>= b
print(a)

a &= b
print(a)

a |= b
print(a)

a ^= b
print(a)

